import prisma from '../prisma';

export function findMethodsByUser(userId: number) {
  return prisma.userPaymentMethod.findMany({ where: { userId }, include: { paymentMethod: true } });
}

export function createUserPaymentMethod(data: any) {
  return prisma.userPaymentMethod.create({ data, include: { paymentMethod: true } });
}

export function deleteUserPaymentMethod(id: number) {
  return prisma.userPaymentMethod.delete({ where: { id } });
}
